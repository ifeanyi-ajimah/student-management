     */
    public function store(Request $request)
    {
        $this -> validate($request, [
            'program'=> 'required',
            'description' => 'required',
            
            ]);
        $prog = new Program;
        $prog ->program = $request->program;
        $prog ->description = $request->description;
        $prog ->save();
        session()->flash('notif', 'successfully saved');
        return back();
    }

